import { build } from 'esbuild'
import fs from 'node:fs/promises'
import { createRequire } from 'node:module'
import os from 'node:os'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const require = createRequire(import.meta.url)
const mobileRoot = path.resolve(__dirname, '..')
const repoRoot = path.resolve(mobileRoot, '..', '..')
const entryPoint = path.join(mobileRoot, 'src/components/workspace/MobileWysiwygTentapWebEditor.tsx')
const generatedFile = path.join(mobileRoot, 'src/components/workspace/MobileWysiwygTentapEditorHtml.ts')
const mobileReactResolver = {
  name: 'mobile-react-resolver',
  setup(build) {
    build.onResolve({ filter: /^react($|\/)|^react-dom($|\/)/ }, (args) => ({
      path: require.resolve(args.path, { paths: [mobileRoot] }),
    }))
  },
}

const tempDir = await fs.mkdtemp(path.join(os.tmpdir(), 'tolaria-mobile-wysiwyg-editor-'))
const bundleFile = path.join(tempDir, 'editor.js')

await build({
  absWorkingDir: repoRoot,
  bundle: true,
  define: {
    'process.env.NODE_ENV': '"production"',
  },
  entryPoints: [entryPoint],
  external: ['expo-constants'],
  format: 'iife',
  jsx: 'automatic',
  minify: true,
  outfile: bundleFile,
  platform: 'browser',
  plugins: [mobileReactResolver],
})

const bundle = await fs.readFile(bundleFile, 'utf8')
await fs.writeFile(generatedFile, generatedEditorHtmlModule(editorHtml(bundle)))

function editorHtml(bundle) {
  return `<!DOCTYPE html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1, maximum-scale=1, user-scalable=no" />
    <title>Tolaria Mobile WYSIWYG Editor</title>
    <style>
      body {
        margin: 0;
        padding: 0;
      }

      #root > div:nth-of-type(1) {
        position: absolute;
        height: 100%;
        overflow: auto;
        width: 100%;
        top: 0;
        bottom: 0;
      }

      #root > div.dynamic-height {
        height: unset;
      }

      #root div .ProseMirror {
        min-height: 100%;
        overflow: visible;
      }

      #root div.dynamic-height .ProseMirror {
        height: unset;
      }

      .ProseMirror:focus {
        outline: none;
      }

      .highlight-background {
        background-color: #e6e6ff;
      }
    </style>
  </head>
  <body>
    <div id="root"></div>
    <script>${bundle}</script>
  </body>
</html>`
}

function generatedEditorHtmlModule(html) {
  return `// Generated by apps/mobile/scripts/build-wysiwyg-editor-html.mjs.
export const mobileWysiwygTentapEditorHtml = ${JSON.stringify(html)}
`
}
